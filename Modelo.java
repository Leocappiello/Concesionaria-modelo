package Modelos;

public class Modelo {

    int id;
    String nombre;
    int anio;

    public Modelo(int id, String nombre, int anio, int marca_id) {
        this.id = id;
        this.nombre = nombre;
        this.anio = anio;
        this.marca_id = marca_id;
    }

    int marca_id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }

    public int getMarca_id() {
        return marca_id;
    }

    public void setMarca_id(int marca_id) {
        this.marca_id = marca_id;
    }

    public Modelo() {

    }



}
